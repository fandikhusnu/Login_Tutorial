    private void tblDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDataMouseClicked
        int baris = tblData.getSelectedRow();
        if (baris != -1) {
            nores.setText(tblData.getValueAt(baris, 0).toString());
            nama.setText(tblData.getValueAt(baris, 1).toString());
            nopol.setText(tblData.getValueAt(baris, 2).toString());
            SimpleDateFormat date = new SimpleDateFormat ("yyyy-MM-dd");
            if ("Motor".equals(tblData.getValueAt(baris, 3).toString())) {
                motor.setSelected(true);
            } else {
                mobil.setSelected(true);
            }
            Date dateFormat1 = null;
            Date dateFormat2 = null;
            try {
                dateFormat1 = date.parse(tblData.getValueAt(baris, 4).toString());
            } catch (ParseException ex) {
                Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                dateFormat2 = date.parse(tblData.getValueAt(baris, 5).toString());
            } catch (ParseException ex) {
                Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
            }
            tglmasuk.setDate(dateFormat1);
            tglkeluar.setDate(dateFormat2);
        }
    }//GEN-LAST:event_tblDataMouseClicked

