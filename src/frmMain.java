    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tglmsk = dateFormat.format(tglmasuk.getDate());
        String tglklr = dateFormat.format(tglkeluar.getDate());
        Date masuk = tglmasuk.getDate();
        Date keluar = tglkeluar.getDate();
        long miliseconds = keluar.getTime() - masuk.getTime();
        int days = (int)miliseconds/(1000*60*60*24);
        int total = days * 3000;    
        String jenis = "";
        String Nama = nama.getText();
        String Nores = nores.getText();
        String Nopol = nopol.getText();
        String hari = "3000";
        
        if (motor.isSelected()) {
            jenis = "Motor";
        } else {
            jenis = "Mobil";
        }
        if (nama.getText().equals("") || 
           nores.getText().equals("") ||
           nopol.getText().equals("") ||
           tglmsk.equals("") ||
           tglklr.equals("") ||
           jenis.equals("")){
            
            JOptionPane.showMessageDialog(this, "Harap Lengkai Data", "Error",
                    JOptionPane.WARNING_MESSAGE);
        }else{            
            String SQL ="INSERT INTO tb_penitipan (NoResi,NamaPetugas,NOPOL,JenisKendaraan,TglMasuk,TglKeluar,HargaHari,HargaTotal)" 
                    + "VALUES('"
                    +nores.getText()+"','"
                    +nama.getText()+"','"
                    +nopol.getText()+"','"
                    +jenis+"','"
                    +tglmsk+"','"
                    +tglklr+"','"
                    +hari+"','"
                    +total+"')";
            int status = KoneksiDB.execute(SQL);
            if (status == 1) {
                 JOptionPane.showMessageDialog(this, "Data berhasil diupdate","Sukses", JOptionPane.INFORMATION_MESSAGE);
                 selectData();
            }else{
                JOptionPane.showMessageDialog(this, "Data gagal diupdate", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
    }                                    

