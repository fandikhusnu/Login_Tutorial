    private void selectData() {
        String kolom[] = {"NoResi","NamaPetugas","NOPOL","JenisKendaraan","TglMasuk","TglKeluar","HargaHari","HargaTotal"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_penitipan";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            while(rs.next()) {
                String NoResi = rs.getString(1);
                String NamaPetugas = rs.getString(2);
                String NOPOL = rs.getString(3);
                String JenisKendaraan = "";
                if ("Motor".equals(rs.getString(4))){
                    JenisKendaraan = "Motor";
                } else {
                    JenisKendaraan = "Mobil";
                }
                String TglMasuk = rs.getString(5);
                String TglKeluar = rs.getString(6);
                String HargaHari = rs.getString(7);
                String HargaTotal = rs.getString(8);
                String data[] = {NoResi,NamaPetugas,NOPOL,JenisKendaraan,TglMasuk,TglKeluar,HargaHari,HargaTotal};
                dtm.addRow(data);
            }
        } catch (SQLException ex){
            Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblData.setModel(dtm);
    }

    private void setTanggal() {
        java.util.Date skrg = new java.util.Date();
        java.text.SimpleDateFormat kal = new
        java.text.SimpleDateFormat("dd/MM/yyyy");
        Tanggal.setText(kal.format(skrg));
    }

    private void setJam() {
        ActionListener taskPerformer = new ActionListener() {
            public void actionPerformed(ActionEvent evt) {
                Date dt = new Date();
                int nilai_jam = dt.getHours();
                int nilai_menit = dt.getMinutes();
                int nilai_detik = dt.getSeconds();
                if (nilai_jam <= 9) {
                    nol_jam = "0";
                }
                if (nilai_menit <= 9) {
                    nol_menit = "0";
                }
                if (nilai_detik <= 9) {
                    nol_detik = "0";
                }
                String jam = nol_jam + Integer.toString(nilai_jam);
                String menit = nol_menit + Integer.toString(nilai_menit);
                String detik = nol_detik + Integer.toString(nilai_detik);
                Waktu.setText("Jam "+jam + ":" + menit + ":" + detik);
            }
        };
        new Timer(100, taskPerformer).start();
    }
